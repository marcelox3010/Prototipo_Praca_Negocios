				<p><div align='justify'><li> Indiretamente</div></p>
				
				
				<p><div align='justify'>Exportação de forma INDIRETA é quando a operação do processo de exportação é executada por um interveniente, uma empresa mercantil, que adquire mercadorias no mercado interno para posterior exportação, como por exemplo, as Comercial Exportadoras e as Trading Company.</div></p>
				
				<p><div align='justify'>Se quiser saber mais, o  Ministério do Desenvolvimento, Indústria e Comércio – MDIC disponibiliza orientações sobre como comercializar de forma indireta.</div></p>
				
