<p><div align='justify'>Entre janeiro e dezembro de 2013, as compras do governo federal movimentaram R$ 68,4 bilhões na aquisição de bens e serviços, dos quais, R$ 20,5 bilhões (30%), referem-se às contratações junto as MPE. Na comparação com o mesmo período de 2012, as MPE aumentaram sua participação nas compras públicas em 33%.</p></div>

<p><div align='justify'><b>Como vender para o Governo Federal </b></p></div>

<p><div align='justify'>Para vender para os órgãos/entidades da Administração Pública Federal Direta, Autárquica e Fundacional do Governo Federal, o usuário deve solicitar acesso ao Sistema Unificado de Cadastramento de Fornecedores – SICAF, que é um sistema desenvolvido em plataforma web, sendo acessado pelo endereço <a href='http://www.comprasgovernamentais.gov.br/' target='_blank'>http://www.comprasgovernamentais.gov.br/</a>, viabilizando o cadastramento de fornecedores de materiais e serviços no âmbito do Sistema Integrado de Serviços Gerais – SISG.</p></div>
<p><div align='justify'>Com o acesso autorizado, a empresa deve iniciar o procedimento de registro cadastral, inserindo as informações constantes dos documentos que serão posteriormente apresentados à Unidade Cadastradora.</p></div>
<p><div align='justify'>Todos os procedimentos constam de manuais disponíveis na área “Fornecedor” do site.</p></div>
<p><div align='justify'>O cadastro no SICAF possui seis níveis, mas somente o preenchimento do nível I, com a validação do cadastro/credenciamento do Fornecedor pela Unidade Cadastradora, é obrigatório para que o fornecedor seja considerado cadastrado no SICAF. Os demais níveis devem ser preenchidos, conforme a necessidade do fornecedor:</p></div>
<p><div align='justify'>• Nível I – Credenciamento;
<p><div align='justify'>• Nível II – Habilitação Jurídica;</p></div>
<p><div align='justify'>• Nível III – Regularidade Fiscal Federal;</p></div>
<p><div align='justify'>• Nível IV – Regularidade Fiscal Estadual/Municipal;</p></div>
<p><div align='justify'>• Nível V – Qualificação Técnica; e</p></div>
<p><div align='justify'>• Nível VI – Qualificação Econômica- Financeira.</p></div>
<p><div align='justify'>A empresa poderá solicitar/validar todos os níveis de uma só vez, de acordo com sua necessidade, pois os mesmos serão solicitados nas demais fases da licitação.</p></div>

<p><div align='justify'>Veja aqui o passo a passo</p></div>

<p><div align='justify' style='background-color:#dddddd'>1º Passo - O fornecedor deve solicitar login e senha para iniciar os procedimentos relativos ao pré-cadastramento no SICAF através do Portal de Compras do Governo Federal – <a href='http://www.comprasgovernamentais.gov.br/' target='_blank'>http://www.comprasgovernamentais.gov.br/</a></div></p>
<p><div align='justify' style='background-color:#dddddd'>2º Passo - Após confirmar o pré-cadastramento no SICAF, o fornecedor deve encaminhar-se à uma Unidade Cadastradora, para a efetivação do cadastramento no SICAF; Neste ato, apresente a documentação exigida, original ou cópia autenticada. </div></p>
<p><div align='justify' style='background-color:#dddddd'>3º Passo - Após a elaboração e efetivação do seu cadastro, de posse do número do protocolo fornecido pela unidade cadastradora, visite o Portal do Governo Federal e verifique os diversos serviços aos Fornecedores e os manuais de orientação.</div></p>
<p><div align='justify' style='background-color:#dddddd'>4º Passo – Identificação do certame para participação e download do edital para leitura detalhada</div></p>
<p><div align='justify' style='background-color:#dddddd'>5º Passo – Envio de Propostas, que consiste em encaminhar proposta de preços por meio do Sistema Eletrônico a partir da data de liberação do edital até o horário limite de abertura da Sessão Pública.</div></p>
<p><div align='justify' style='background-color:#dddddd'>6º Passo – Consulta horário de abertura para envio de lances, bem como encerramento, no sistema eletrônico ou no edital.</div></p>
<p><div align='justify' style='background-color:#dddddd'>7º Passo – Envio de Lances conforme horário definido.</div></p>


				
				
