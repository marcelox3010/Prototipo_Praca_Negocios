					<p><div align='justify'><li> Registro de Exportação – RE e Declaração de Despacho de Exportação - DDE</div></p>
					<p><div align='justify'>A RE é o registro eletrônico das informações de natureza comercial, financeira, cambial e fiscal que caracterizam a operação de exportação de uma mercadoria. A DDE, obtida por meio eletrônico, é o procedimento fiscal mediante o qual se processa o desembaraço aduaneiro da mercadoria destinada ao exterior, seja ela exportada a título definitivo ou não. </div></p>
					<p><div align='justify'>Após efetivado o Registro de Exportação – RE e com mercadoria pronta para o embarque a empresa, de posse dos documentos exigidos para a exportação, deve providenciar a Declaração do Despacho de Exportação - DDE, por meio do SISCOMEX.</div></p>
					<p><div align='justify'>O Registro no SISCOMEX é  feito pelo exportador ou por seu representante legal.</div></p>
					
					<p><div align='justify' style='background-color:#dddddd'>Conheça mais sobre registro de exportação RE. <a href='http://www.receita.fazenda.gov.br/manuaisweb/exportacao/topicos/procedimentos-preliminares/Controle-Adm-Secex/RE.htm' target='_blank'>Clique aqui</a></div></p>
					
					<p><div align='justify' style='background-color:#dddddd'>Conheça mais orientações e informações sobre Declaração de Despacho de Exportação (DDE) e seu registro no SICOMEX. <a href='http://www.mdic.gov.br/sistemas_web/aprendex/default/index/conteudo/id/206' target='_blank'>Clique aqui</a></div></p>

					
					<p><div align='justify'><li> Declaração Simplificada de Exportação - DSE</div></p>
					<p><div align='justify'>A DSE foi criada pelo Governo com o objetivo de simplificar os despachos aduaneiros de mercadorias de baixo valor e estimular as exportações, em especial das micro e pequenas empresas.</div></p>
					<p><div align='justify'>Em situações tratadas na Instrução Normativa SRF 611/06, desde que não sujeitas a controle específico de órgãos anuentes, o exportador poderá optar pelo registro de Declaração Simplificada de Exportação (DSE), prescindindo do registro do RE. </div></p>
					
					
					<p><div align='justify' style='background-color:#dddddd'>Conheça mais sobre Declaração Simplificada de Exportação (DSE). <a href='http://www.receita.fazenda.gov.br/manuaisweb/exportacao/topicos/elaboracao-da-declaracao/dse.htm' target='_blank'>Clique aqui</a></div></p>
					
					
					
					
					<p><div align='justify'><li>	Desembaraço Aduaneiro</div></p>
					<p><div align='justify'>É aonde ocorre a seleção parametrizada, que estabelece níveis diferenciados de conferência aduaneira para a declaração de exportação.</div></p>
					<p><div align='justify'>A execução da seleção parametrizada no caso de Declaração de Exportação - DE ocorre após o envio da declaração para despacho.</div></p>
					<p><div align='justify'>A seleção parametrizada poderá ser executada de forma automática, em horários previamente estabelecidos pela Unidade da Receita Federal, ou de forma imediata a qualquer momento, a critério do supervisor do recinto aduaneiro. </div></p>
					<p><div align='justify'>Na seleção parametrizada há três canais de conferência para a Declaração de Exportação - DE:</div></p>


					<p><div align='justify'><font color='green'>CANAL VERDE</font> - o sistema procederá ao desembaraço automático da declaração, não sendo obrigatória a conferência aduaneira.</div></p>

					<p><div align='justify'><font color='orange'>CANAL LARANJA</font> - procedimento obrigatório: exame documental efetuado pela fiscalização aduaneira (arts. 22 a 24 da Instrução Normativa SRF nº 28, de 1994). </div></p>

					<p><div align='justify'><font color='red'>CANAL VERMELHO</font> - procedimentos obrigatórios:</div></p>
					<p><div align='justify'>Exame documental efetuado pela fiscalização aduaneira (arts. 22 a 24 da Instrução Normativa SRF nº 28, de 1994).</div></p>
					<p><div align='justify'>Verificação da mercadoria efetuado pela fiscalização aduaneira (arts. 25 a 28 da Instrução Normativa SRF nº 28, de 1994).</div></p>

					<p><div align='justify'>A Declaração de Exportação - DE selecionada para o canal verde ou laranja poderá ser redirecionada, quando por determinação da autoridade aduaneira, para o canal vermelho.</div></p>


