					<p><div align='justify'><li> NCM - Nomenclatura Comum do Mercosul, é um dos métodos internacionais de classificação de mercadorias, criado de acordo com a definição estabelecida entre os países do Mercosul. A NCM é composta de oito dígitos, sendo os seis primeiros formados pelo Sistema Harmonizado - SH (capítulo, posição e subposição), e os dois últimos (item e subitem).</div></p>

					<p><div align='justify'>É importante que o exportador faça a correta classificação dos produtos, com a finalidade de evitar a aplicação de penalidades pelas autoridades aduaneiras, além de utilizar as vantagens tarifárias decorrentes dos acordos bilaterais e multilaterais que o Brasil mantém no âmbito de seu comércio internacional. </div></p>
					<p><div align='justify'>Classificar corretamente sua mercadoria traz vantagens como a redução do Imposto de Importação, ou até mesmo em sua isenção, de acordo com os acordos comerciais vigentes. Neste sentido, é necessário que o exportador conheça os benefícios tributários do seu produto em relação ao mercado brasileiro, a fim de ganhar competitividade frente aos concorrentes de outros países que eventualmente não sejam favorecidos pelos tratados comerciais que o Brasil mantém no seu comércio exterior.</div></p>
					<p><div align='justify'>Essa vantagem tributária será efetivamente formalizada durante o processo do despacho aduaneiro, quando o importador deverá estar de posse do Certificado de Origem, para eventual apresentação às autoridades aduaneiras, documento esse emitido pela entidade autorizada no país do exportador, no qual devem constar os fundamentos legais do acordo comercial que está sendo aproveitado nessa operação. A falta de apresentação do certificado de origem ocasiona a perda dessas vantagens, implicando o pagamento pelo importador do Imposto de Importação com as alíquotas normais.</div></p>

