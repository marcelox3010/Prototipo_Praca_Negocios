<p><div align='justify'>Para a habilitação nas licitações exigir-se-á dos interessados, conforme artigo 27 da  lei 8.666/93,exclusivamente, documentação relativa a:</p></div>
<p><div align='justify'><b>Habilitação Jurídica</b> - são documentos como registro comercial ou contrato social da empresa.</p></div>

<p><div align='justify'><b>Regularidade Fiscal</b> - são documentos como inscrição no Cadastro Geral de Contribuintes e provas de regularidade na Fazenda Pública (federal, estadual e municipal) e Seguridade Social.</p></div>

<p><div align='justify'><b>Qualificação Técnica</b> - são documentos como inscrição na entidade profissional competente e comprovação de aptidão para a atividade assinada por terceiros.</p></div>

<p><div align='justify'><b>Qualificação Econômico-Financeira</b> - são documentos como balanço patrimonial e demonstração financeira da empresa e certidão negativa de falência.</p></div>


