<p><div align='justify'>Os Governos Estaduais e o Distrito Federal são grandes compradores de bens e serviços de micro e pequenas empresas. Muitos governos possuem seu próprio portal de compras, porém algum deles pode utilizar outros portais de compras coletivos ou o portal do Governo Federal. Fique atento aos procedimentos e regras das licitações públicas de cada Estado, pois elas podem variar em função das rotinas utilizadas, bem como em função de legislações estaduais. </p></div>

<p><div align='justify'>Para participar de licitações em seu Estado, verifique os procedimentos necessários no site de compras utilizado ou na própria unidade responsável pelas compras.</p></div>
<p><div align='justify'>Importante ressaltar que toda a documentação solicitada deve estar prevista em Lei, seja na nº 8.666/93, ou em legislação específica, além de estarem descritos no instrumento convocatório.</p></div>
<p><div align='justify'>Importante estar atento e realizar uma leitura detalhada do edital, pois ele regerá todas as ações no processo licitatório.</p></div>


